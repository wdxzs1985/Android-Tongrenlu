package info.tongrenlu.app;

public interface CommonConstants {

    public int ZERO = 0;

    public int ONE = 1;

    public int TWO = 2;

    public int FOUR = 4;

    public int SIX = 6;

    public int SEVEN = 7;

    public int TEN = 10;

    public int TWENTY_FOUR = 24;

    public int SIXTY = 60;

    public long SECOND = 1000;

    public long MINUTE = SIXTY * SECOND;

    public long HOUR = SIXTY * MINUTE;

    public long DAY = TWENTY_FOUR * HOUR;

    public long WEEK = SEVEN * DAY;

}
